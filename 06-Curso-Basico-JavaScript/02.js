/* Tipos de datos */

// Valores primitivos

40 // Valor tipo número
"40" // Valor tipo string

/* Valores para tomar decisiones */
true // Valor tipo booleano George Boole
false // Valor tipo booleano

/* Valores para reservar o producto de un error*/
null // Valor tipo nulo
undefined // Valor tipo indefinido

// Valores no primitivos u objetos
[1, 2, 3, 4] // Valor tipo arreglo
{nombre: "Estefanía"} // Valor tipo objeto

// Para saber el tipo de dato de un valor
console.log(typeof 40); // number
console.log(typeof(40)); // number
console.log(typeof "40"); // string
console.log(typeof true); // boolean
console.log(typeof null); // object
console.log(typeof undefined); // undefined
console.log(typeof [1, 2, 3, 4]); // object
console.log(typeof {nombre: "Estefanía"}); // object







